                        <h3 class="section-title">Tentang kami</h3>
                        <p>PT. Primecoconesia adalah perusahaan yang bergerak di bidang pengolahan kelapa yang fokus pada produksi minyak kelapa berkualitas tinggi. Tidak hanya menjadi produsen minyak kelapa berkualitas tinggi PT. Primecoconesia juga menjadi produsen berbagai produk turunan kelapa berkualitas tinggi, seperti: Kapuk, Briket, Cocopeat, Cocofiber dll.</p>
                        <p>Berawal dari usaha rumahan di daerah sulawesi selatan yang mengolah kelapa dan turunannya menjadi produk bernilai, akhirnya dari bekal pengalaman yang dimiliki kami mulai masuk ke skala yang lebih besar.</p>
                        <p>Akhirnya kami mempelajari potensi sumber daya pertanian khususnya kelapa di wilayah Sulawesi dan Jawa. Kemudian menemukan potensi produksi kelapa terbaik di berbagai daerah, dari temuan tersebut PT. Primecoconesia mulai melakukan pengolahan kelapa dalam skala besar menjadi minyak kelapa berkualitas tinggi dan memanfaatkan berbagai jenis turunan kelapa menjadi produk bernilai tinggi, seperti: Kopra, Sabut, Kapuk, Briket, Cocopeat, Cocofiber dll.</p>
                        <p>Kami membangun kerjasama dan memberdayakan petani di berbagai wilayah di Indonesia (Sulawesi Selatan, Sulawesi Utara, Sulawesi Barat, Sulawesi Tenggara, Jawa Timur, Jawa Tengah dan Jawa Barat) untuk menghasilkan kelapa berkualitas tinggi dengan harga terbaik, dari kelapa berkualitas tinggi dihasilkan produk bernilai tinggi, seperti: minyak kelapa goreng,minyak kelapa murni, kopra, briket/arang, kapuk, cocopeat, cocofiber dan berbagai produk bernilai lainnya.</p>
                        <p>Kami memproduksi kelapa dan turunannya melalui proses kerja terbaik agar mutu dan kualitas produk yang dihasilkan menjadi produk berkualitas tinggi. Proses kerja tersebut dilaksanakan secara bertahap, mulai dari proses penanaman, pembibitan, pemeriksaan, pengelohan hingga tercipta produk berkualitas tinggi dengan harga terbaik.</p>
                        <p>Kami memiliki lahan perkebunan kelapa seluas 10.271 Hektar (Ha) dengan total produksi 20.356 ton/pertahun atau 5.089 ton/triwulan yang tersebar di berbagai wilayah di Indonesia (Sulsel-Sulbar-Sulteng-Sultra-Jatim-Jabar dan Jateng) yang di panen langsung oleh petani setempat dengan kualitas terbaik.</p>
                        <p>Setelah merasakan keutungan dan melihat peluang yang menjanjikan serta permintaan yang terus meningkat dari waktu ke waktu, didukung dengan sumber daya kelapa yang melimpah di Indonesia maka PT. Primecoconesia berkomitmen untuk melakukan pengembangan dan perluasan dalam bidang produksi minyak kelapa berkualitas tinggi dan berbagai produk turunan kelapa lainnya.</p>
                        <p>Produk turunan kelapa yang kami produksi meliputi kopra, batok kelapa, daun kelapa, sabut kelapa. Kami bekerjasama dengan investor untuk mengolah limbah turunan kelapa menjadi produk yang memiliki nilai produksi dan nilai jual, seperti cocofiber, cocopeat, arang kelapa/briket dan sebagainya.</p>
                        <p>Saat ini kami sedang melakukan ekspansi pasar, memperluas komuditas dan menarik investor untuk memperbesar tempat pengolahan dan meningkatkan kuantitas serta kualitas produk. Kami juga menarik investor asing untuk melakukan ekspor ke kawasan eropa, asia pasifik, timur tengah, amerika dan sebagainya.</p>
